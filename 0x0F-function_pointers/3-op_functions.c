#include "3-calc.h"
/**
 * op_add - addition
 * @a: first
 * @b: second
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substraction
 * @a: first
 * @b: second
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication
 * @a: firt
 * @b: second
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: first
 * @b: second
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo
 * @a: first
 * @b: second
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
